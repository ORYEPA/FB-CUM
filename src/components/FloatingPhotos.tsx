export default function FloatingPhotos() {
  const items = useMemo(() => Array.from({ length: 24 }, (_, idx) => {
    const base = 52 + (idx % 4) * 14
    const size = Math.round(base + Math.random() * base * 0.3)
    return {
      id: idx,
      src: PHOTOS[idx % PHOTOS.length],
      left: (idx * 13 + 3) % 93,
      delay: idx * 0.9,
      duration: 13 + (idx % 7) * 2.5,
      size,
    }
  }), [])

  return (
    <div className="bg-photos" aria-hidden="true">
      {items.map(item => (
        <img
          key={item.id}
          src={item.src}
          alt=""
          className="bg-photo"
          style={{
            left: item.left + '%',
            animationDelay: item.delay + 's',
            animationDuration: item.duration + 's',
            width: item.size + 'px',
            height: item.size + 'px',
          }}
        />
      ))}
    </div>
  )
}
